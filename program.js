// parse rest string "3 мин" / "30 сек" / "3-4 мин" / "—" -> seconds (default 120)
function restToSeconds(rest) {
  if (!rest || rest === "—" || rest === "-") return 0;
  const s = rest.toLowerCase();
  const m = s.match(/(\d+)(?:-(\d+))?\s*(мин|сек|min|sec|s)/);
  if (!m) return 120;
  const n = m[2] ? (parseInt(m[1]) + parseInt(m[2])) / 2 : parseInt(m[1]);
  const unit = m[3];
  return unit.startsWith("м") || unit === "min" ? n * 60 : n;
}

